<h1>Política de Privacidade do RachaRole</h1>
    <p>Última atualização: 28/07/2024</p>
    <p>Bem-vindo ao RachaRole! A sua privacidade é importante para nós. Esta Política de Privacidade explica como coletamos, usamos, divulgamos e protegemos suas informações quando você usa nosso aplicativo de divisão de despesas, RachaRole. Ao utilizar o RachaRole, você concorda com as práticas descritas nesta política.</p>

    <h2>1. Informações que Coletamos</h2>
    <p>Nós coletamos os seguintes tipos de informações:</p>

    <h3>1.1. Informações Pessoais</h3>
    <ul>
        <li><strong>Nome:</strong> Coletamos seu nome para identificar e personalizar sua conta.</li>
        <li><strong>E-mail:</strong> Utilizamos seu e-mail para comunicação e recuperação de senha.</li>
        <li><strong>Informações de Contato:</strong> Opcionalmente, você pode fornecer informações adicionais de contato.</li>
    </ul>

    <h3>1.2. Informações de Uso</h3>
    <ul>
        <li><strong>Dados de Uso:</strong> Coletamos informações sobre como você utiliza o aplicativo, incluindo suas interações com outros usuários e o histórico de despesas criadas e divididas.</li>
    </ul>

    <h2>2. Como Utilizamos as Informações</h2>
    <p>Utilizamos as informações coletadas para os seguintes fins:</p>
    <ul>
        <li><strong>Fornecimento do Serviço:</strong> Para permitir que você crie e gerencie suas despesas e as divida com seus amigos.</li>
        <li><strong>Comunicação:</strong> Para enviar notificações, atualizações e responder a suas dúvidas e solicitações.</li>
        <li><strong>Melhoria do Serviço:</strong> Para entender como o aplicativo é usado e implementar melhorias e novos recursos.</li>
    </ul>

    <h2>3. Compartilhamento de Informações</h2>
    <p>Não compartilhamos suas informações pessoais com terceiros, exceto nas seguintes circunstâncias:</p>
    <ul>
        <li><strong>Com Seu Consentimento:</strong> Podemos compartilhar informações com terceiros se você nos der permissão explícita para fazê-lo.</li>
        <li><strong>Para Cumprimento da Lei:</strong> Podemos divulgar suas informações se exigido por lei ou em resposta a uma ordem judicial.</li>
    </ul>

    <h2>4. Segurança das Informações</h2>
    <p>Adotamos medidas de segurança adequadas para proteger suas informações contra acesso, alteração, divulgação ou destruição não autorizados. No entanto, lembre-se de que nenhum método de transmissão pela internet ou armazenamento eletrônico é 100% seguro.</p>

    <h2>5. Retenção de Dados</h2>
    <p>Nós retemos suas informações pelo tempo necessário para cumprir os propósitos descritos nesta política, a menos que um período de retenção mais longo seja exigido ou permitido por lei.</p>

